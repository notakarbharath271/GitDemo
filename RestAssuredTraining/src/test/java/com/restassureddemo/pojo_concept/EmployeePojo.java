package com.restassureddemo.pojo_concept;

import java.util.List;

public class EmployeePojo {
	
	private String employee_name ;
	private long employee_salary;
	private int employe_age;
	private List Skills;
	public String getEmployee_name() {
		return employee_name;
	}
	public void setEmployee_name(String employee_name) {
		this.employee_name = employee_name;
	}
	public long getEmployee_salary() {
		return employee_salary;
	}
	public void setEmployee_salary(long employee_salary) {
		this.employee_salary = employee_salary;
	}
	public int getEmploye_age() {
		return employe_age;
	}
	public void setEmploye_age(int employe_age) {
		this.employe_age = employe_age;
	}
	public List getSkills() {
		return Skills;
	}
	public void setSkills(List skills) {
		this.Skills = skills;
	}
	
	
	
	

}
